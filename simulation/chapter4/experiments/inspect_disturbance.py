"""Inspect Chapter 4 disturbance signals (wave / residual / total and observer estimates).

This script is meant for quick sanity checks:
- What does the injected wave disturbance look like (time domain + spectrum)?
- How big is the residual (unmodeled + numerical) term vs wave?
- Do ESO/STESO track wave or only parts of total?

Input:
- An NPZ generated by `generate_disturbance_data.py`.

Outputs:
- Prints summary stats to stdout.
- Saves a figure next to the NPZ (or --out-dir):
  - <stem>_disturbance_overview.png

Usage examples:
- Auto-pick latest NPZ:
    python simulation/chapter4/experiments/inspect_disturbance.py

- Specify NPZ:
    python simulation/chapter4/experiments/inspect_disturbance.py --npz path/to/file.npz

Notes:
- Frequency plot uses a simple one-sided FFT magnitude. This is *not* a rigorous PSD
  estimator, but it's good enough to see dominant bands and whether residual is
  high-frequency dominated.
"""

from __future__ import annotations

import argparse
import os
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

import numpy as np
import matplotlib.pyplot as plt


DOF_LABELS = ["z", "alpha", "beta"]


@dataclass
class Signals:
    time: np.ndarray  # (T,)
    actual: Optional[np.ndarray] = None  # (T,3)
    wave: Optional[np.ndarray] = None  # (T,3)
    residual: Optional[np.ndarray] = None  # (T,3)
    eso: Optional[np.ndarray] = None  # (T,3)
    steso: Optional[np.ndarray] = None  # (T,3)


def _find_latest_npz(fig_dir: Path) -> Optional[Path]:
    if not fig_dir.exists():
        return None
    candidates = sorted(fig_dir.glob("chapter4_disturbance_data_*.npz"), key=lambda p: p.stat().st_mtime)
    return candidates[-1] if candidates else None


def _load_npz(path: Path) -> Signals:
    d = np.load(str(path), allow_pickle=True)

    if "time" not in d.files:
        raise KeyError(f"Missing 'time' in {path}. Keys: {d.files}")
    time = np.asarray(d["time"], dtype=np.float64)

    def pick(*keys: str) -> Optional[np.ndarray]:
        for k in keys:
            if k in d.files:
                arr = np.asarray(d[k], dtype=np.float64)
                return arr
        return None

    actual = pick("actual_disturbance", "d_actual", "actual_dist")
    wave = pick("wave_disturbance", "wave")
    residual = pick("residual_disturbance", "residual")
    if residual is None and actual is not None and wave is not None:
        residual = actual - wave

    eso = pick("eso_disturbance", "eso_dist", "eso")
    steso = pick("steso_disturbance", "steso_dist", "steso")

    # Basic shape checks (only if present)
    for name, arr in (
        ("actual", actual),
        ("wave", wave),
        ("residual", residual),
        ("eso", eso),
        ("steso", steso),
    ):
        if arr is None:
            continue
        if arr.ndim != 2 or arr.shape[1] != 3:
            raise ValueError(f"{name} must be (T,3). Got {arr.shape} from {path}")
        if len(arr) != len(time):
            raise ValueError(f"{name} length mismatch: len(time)={len(time)} vs {arr.shape[0]}")

    return Signals(time=time, actual=actual, wave=wave, residual=residual, eso=eso, steso=steso)


def _rms(x: np.ndarray) -> np.ndarray:
    return np.sqrt(np.mean(np.square(x), axis=0))


def _fft_mag(x: np.ndarray, dt: float) -> tuple[np.ndarray, np.ndarray]:
    """Return one-sided frequency axis and magnitude spectrum for each DOF.

    x: (T,3)
    """
    n = x.shape[0]
    x0 = x - np.mean(x, axis=0, keepdims=True)
    # rfft: (Nf,3)
    X = np.fft.rfft(x0, axis=0)
    mag = np.abs(X) / max(n, 1)
    f = np.fft.rfftfreq(n, d=dt)
    return f, mag


def _summary_block(name: str, x: Optional[np.ndarray]) -> str:
    if x is None or len(x) == 0:
        return f"- {name}: (missing)"
    norm = np.linalg.norm(x, axis=1)
    rms = _rms(x)
    q = np.quantile(norm, [0.5, 0.9, 0.99])
    return (
        f"- {name}: mean||·||={norm.mean():.3g}, max||·||={norm.max():.3g}, "
        f"median/p90/p99={q[0]:.3g}/{q[1]:.3g}/{q[2]:.3g}, "
        f"RMS(z,a,b)={rms[0]:.3g},{rms[1]:.3g},{rms[2]:.3g}"
    )


def plot_overview(sig: Signals, out_path: Path, title: str = "") -> None:
    time = sig.time
    if len(time) < 2:
        raise ValueError("Need at least 2 time samples")
    dt = float(np.median(np.diff(time)))

    # Figure layout: 3 rows (DOF). For each DOF: time series + spectrum.
    fig, axes = plt.subplots(3, 2, figsize=(14, 9), sharex="col")

    series = []
    if sig.actual is not None:
        series.append(("actual", sig.actual))
    if sig.wave is not None:
        series.append(("wave", sig.wave))
    if sig.residual is not None:
        series.append(("residual", sig.residual))
    if sig.eso is not None:
        series.append(("eso", sig.eso))
    if sig.steso is not None:
        series.append(("steso", sig.steso))

    # Time-domain plots
    for i in range(3):
        ax = axes[i, 0]
        for name, arr in series:
            ax.plot(time, arr[:, i], label=name, linewidth=1.1)
        ax.set_ylabel(f"{DOF_LABELS[i]} force")
        ax.grid(True, alpha=0.25)
        if i == 0:
            ax.legend(loc="best", ncol=3)

    axes[-1, 0].set_xlabel("time (s)")

    # Spectrum plots (log-y)
    for i in range(3):
        ax = axes[i, 1]
        for name, arr in series:
            f, mag = _fft_mag(arr, dt=dt)
            ax.plot(f, mag[:, i], label=name, linewidth=1.0)
        ax.set_yscale("log")
        ax.set_ylabel(f"|FFT| ({DOF_LABELS[i]})")
        ax.grid(True, alpha=0.25)
        ax.set_xlim(0.0, min(50.0, f.max() if len(f) else 50.0))
        if i == 0:
            ax.legend(loc="best", ncol=3)

    axes[-1, 1].set_xlabel("frequency (Hz)")

    if title:
        fig.suptitle(title)
    fig.tight_layout()
    fig.savefig(out_path, dpi=200)
    plt.close(fig)


def main() -> None:
    parser = argparse.ArgumentParser(description="Inspect/plot Chapter4 disturbance signals")
    parser.add_argument(
        "--npz",
        type=str,
        default="",
        help="Path to NPZ. If empty, auto-picks the latest chapter4_disturbance_data_*.npz in chapter4/figures.",
    )
    parser.add_argument(
        "--out-dir",
        type=str,
        default="",
        help="Output directory for figure. Default: same folder as NPZ.",
    )
    parser.add_argument(
        "--title",
        type=str,
        default="Chapter4 disturbance overview",
        help="Figure title",
    )
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[2]
    fig_dir = repo_root / "chapter4" / "figures"

    if args.npz:
        npz_path = Path(args.npz).expanduser().resolve()
    else:
        latest = _find_latest_npz(fig_dir)
        if latest is None:
            raise FileNotFoundError(f"No NPZ found in {fig_dir}")
        npz_path = latest

    sig = _load_npz(npz_path)

    # Print stats
    print(f"NPZ: {npz_path}")
    print(f"T={len(sig.time)}; t=[{sig.time[0]:.3g}, {sig.time[-1]:.3g}] s")
    if len(sig.time) >= 2:
        dt = float(np.median(np.diff(sig.time)))
        print(f"dt≈{dt:.6g} s; fs≈{1.0/dt:.3g} Hz")

    print(_summary_block("wave", sig.wave))
    print(_summary_block("residual", sig.residual))
    print(_summary_block("actual", sig.actual))
    print(_summary_block("eso", sig.eso))
    print(_summary_block("steso", sig.steso))

    # Convenience: correlation with wave
    if sig.wave is not None and sig.steso is not None:
        # per-DOF Pearson corr, avoid zero std
        corr = []
        for i in range(3):
            x = sig.wave[:, i]
            y = sig.steso[:, i]
            sx = np.std(x)
            sy = np.std(y)
            if sx < 1e-12 or sy < 1e-12:
                corr.append(np.nan)
            else:
                corr.append(float(np.corrcoef(x, y)[0, 1]))
        print(f"- corr(wave, steso) per-DOF: z={corr[0]:.3f}, a={corr[1]:.3f}, b={corr[2]:.3f}")

    out_dir = Path(args.out_dir).expanduser().resolve() if args.out_dir else npz_path.parent
    out_dir.mkdir(parents=True, exist_ok=True)

    stem = npz_path.stem
    out_path = out_dir / f"{stem}_disturbance_overview.png"
    plot_overview(sig, out_path=out_path, title=args.title)
    print(f"Saved: {out_path}")


if __name__ == "__main__":
    main()
